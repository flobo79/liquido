<p class="bigheadline">Startbildschirm</p>
<p>Nachdem Sie sich eingeloggt haben, gelangen Sie auf den Startbildschirm. Hier 
  sehen Sie alle Bereiche die Ihnen zur Verf&uuml;gung stehen. Am oberen Bildschirmrand 
  befindet sich die Navigationsleiste, in der Sie die diese Schaltf&auml;chen 
  sehen: </p>
<p><img src="<?php echo "components/$topic" ?>/help/gfx/menubar.gif" width="251" height="37"> </p>
<p>Mit &quot;Start&quot; gelangen Sie immer auf den Startbildschirm zur&uuml;ck, 
  durch klicken auf &quot;Hilfe&quot; wird die Hilfe f&uuml;r den Bereich angezeigt 
  in dem Sie sich befinden.<br>
  <br>
</p>
<p class="bigheadline">Logout</p>
<p><img src="<?php echo "components/$topic" ?>/help/gfx/logout.gif" width="60" height="82"></p>
<p>Unter den Bereichen befindet sich in der Mitte der &quot;logout&quot;-Button. 
  Damit k&ouml;nnen Sie Ihre Sitzung sicher beenden.</p>
<p>&nbsp;</p>
<p class="bigheadline">Versionsnummer</p>
<p>Am unteren Ende des Startbildschirm befindet sich die Versionsnummer von Liquido. 
  Bei Supportanfragen geben Sie diese bitte immer mit an.</p>
<p>&nbsp;</p>
<p class="bigheadline">Aufbau von Liquido</p>
<p>Bei der Verwendung von Liquido werden Ihnen zwei verschiedene Aufbaumuster 
  begegnen: das horizontale Aufbaumuster und das vertikale Aufbaumuster.</p>
<p>Horizontaler Aufbau</p>
<p><img src="components/start/help/gfx/ha.gif" width="400" height="300"></p>
<p>Im Horizontalen Aufbau wird der Arbeitsbereich in drei Spalten geteilt. In 
  der linken Spalte befindet sich die Objektauswahl, in der Mitte der Arbeitsbereich 
  und in der rechten Spalte der Werkzeug- und Options-Bereich.</p>
<p>Vertikaler Aufbau</p>
<p><img src="components/start/help/gfx/va.gif" width="400" height="289"></p>
<p>Im Vertikalen Aufbau ist die Arbeitsfl&auml;che horizontal in zwei Teile geteilt.</p>
