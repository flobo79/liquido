<p class="bigheadline">Erstellen neuer Formulare </p>
<p>Um ein neues Formular zu erstellen, gehen Sie in in den Bereich &quot;Informationen&quot;,
  scrollen dort bis ans Ende der Seite und klicken dann auf &quot;Neues
  Formular anlegen&quot;. Sie werden auf eine Seite gef&uuml;hrt, auf der Sie einen Namen
  f&uuml;r das Formular eingeben k&ouml;nnen. Nachdem Sie das Formular erstellt haben werden
  Sie auf die Details Seite geleitet, auf der Sie Einstellungen f&uuml;r das Formular
  vornehmen k&ouml;nnen:</p>
<p><strong>Name</strong><br />
Bezeichnung f&uuml;r das Formular</p>
<p><strong>Best&auml;tigtungsnachricht</strong><br />
  Ein Text, der angezeigt wird, nachdem das Formular abgesendet wurde und keine
    Fehler festgestellt wurden. Dieser Text ist &uuml;berfl&uuml;ssig wenn Sie die Option
&quot;nach dem Absenden weiterleiten&quot; aktivieren.</p>
<p><strong>Best&auml;tigungs Link</strong><br />
  Dieser Link wird angezeigt, nachdem das Formular abgesendet wurde und keine
  Fehler festgestellt wurden. Wenn die Option &quot;nach dem Absenden weiterleiten&quot; aktiviert
  ist, wird der Besucher automatisch an diesen Link weitergeleitet.</p>
<p><strong>Option &quot;nach dem Absenden weiterleiten&quot;</strong><br />
Ist diese Option aktiviert, wird der Besucher automatisch an den Best&auml;tigungs
  Link weitergeleitet, nachdem er das Formular richtig ausgef&uuml;llt hat.</p>
<p><strong>Email an Kunden</strong><br />
  Hier kann ein Emailtext eingegeben werden, der dem Kunden zugesendet wird,
    sobald er das Formular richtig ausgef&uuml;llt hat. Dieser Text kann durch Tags
erweitert werden, lesen Sie dazu weiter unten das Kaptiel <a href="#tags">&quot;arbeiten
mit Tags&quot;</a>.</p>
<p><strong>ID f&uuml;r Email-Feld</strong><br />
  Hier muss die ID des Eingabefeldes eingegeben werden, in das der Besucher seine
    Email-Adresse eingegeben hat. Wenn eine Email an den Kunden versendet werden
    soll, ist dieses Feld Pflichtfeld. Die ID erfahren Sie, indem Sie im Compose
Modus die ID des entsprechenden Feldes ablesen.</p>
<p><strong>Absendername:</strong><br />
Diese Angabe wird im Emailprogramm des Besuchers als &quot;Absender&quot; angezeigt.</p>
<p><strong>Absender e-Mail-Adresse</strong><br />
Dies ist die return-mail Adresse, also die Adresse an die eine Email geschickt
  wird, wenn der Besucher auf die empfangene Email antwortet.</p>
<p><strong>Betreff</strong><br />
Dies ist die Betreffzeilenangabe der Email, die der Besucher bekommt.</p>
<p><strong>E-Mail an Formularauswertung</strong><br />
  Dieser Text kann zur Auswertung des Formulares an eine Email Adresse geschickt
    werden. Der Text kann mit Tags dynamisch erweitert werde, lesen Sie dazu
den Absatz <a href="#tags">&quot;Arbeiten mit Tags&quot;</a> weiter unten.</p>
<p><strong>Absender Email Adresse</strong><br />
Dies ist die Antwort-Adresse</p>
<p><strong>Betreff</strong><br />
Dies ist die Betreffzeile der versendeten e-Mail.</p>
