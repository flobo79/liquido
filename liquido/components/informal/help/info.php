<p><strong><a name="tags" id="tags"></a>Arbeiten mit Tags</strong></p>
<p>Die Felder e-Mail an Kunden und e-Mail an Formularauswertung k&ouml;nnen mit
  Eingaben des Besuchers versehen werden, indem in den Text sogenannte Tags (Platzhalter)
  eingef&uuml;gt werden. Ein Tag ist aufgebaut aus einer Raute (#) gefolgt von
  der ID eines Eingabefeldes (zB #e312). Die IDs sind im Compose Modus sichtbar.
  Wird im Text ein Tag eingef&uuml;gt, wird dieser beim Absenden des Formulars
  durch die entsprechende Formulareingabe ausgetauscht. Bitte beachten Sie dass
  zwischen der Raute und der ID kein Leerzeichen sein darf. </p>
