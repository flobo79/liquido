<p>Im Bereich Inhalte werden s&auml;mtlich Seiten und Seitenstrukturen Ihres Projektes 
  erstellt und definiert. </p>
<p class="bigheadline">Kopfleiste</p>
<p>In der Kopfleiste finden Sie neben den Standardschaltfl&auml;chen &quot;Start&quot; 
  und &quot;Hilfe&quot; je nach Verf&uuml;gbarkeit die Schaltfl&auml;chen &quot;Information&quot;, 
  &quot;Ansicht&quot;, &quot;Compose&quot; und &quot;Vorlagen&quot;. Durch klicken 
  auf die jeweilige Schaltfl&auml;che wird der entsprechende Modus geladen. Je 
  nach Modus wird rechts das Options- und Werkzeugfenster geladen.</p>
<p>&nbsp;</p>
<p class="bigheadline">Inhaltsobjektefenster</p>
<p>Am linken Rand finden Sie eine Liste der vorhandenen Inhalte. Dabei sind Gruppen 
  fett dargestellt und Seiten normal. Durch das Klicken auf ein Objekt wird das 
  Objekt ausgew&auml;hlt und in dem Modus angezeigt in dem Sie sich gerade befinden.</p>
<p>&nbsp;</p>
<p class="bigheadline">Gruppen und Seiten</p>
<p>Inhalte sind in Gruppen und Seiten gegliedert. Gruppen unterscheiden sich von 
  Seiten dadurch, das Ihnen unterschiedlich Eigenschaften gegeben werden k&ouml;nnen. 
  So beinhalten Gruppen die Information welche Vorlage verwendet wird. Seiten 
  beinhalten die Inhalte. Je nach Konfiguration k&ouml;nnen Gruppen auch Inhalte 
  enthalten.</p>
<p>&nbsp;</p>
<p class="bigheadline"><img src="gfx/trashcan.gif" width="16" height="17"> 
  Papierkorb</p>
<p>Wenn Sie Inhaltsobjekte l&ouml;schen werden Sie zun&auml;chst in den Papierkorb 
  verschoben. Im Inhaltsobjektefenster ist unten ein Papierkorbsymbol abgebildet, 
  daneben steht ausserdem die Angabe wie viele Objekte sich derzeit darin befinden. 
  Im Papierkorb werden alle Objekte nach Art sortiert aufgelistet und das n&auml;chst 
  &uuml;bergeordnete Objekt in Klammen hinter jedem Objekt dargestellt, um nachzuvollziehen 
  wo das Objekt gel&ouml;scht wurde.<br>
  Sie haben die M&ouml;glichkeit Objekte endg&uuml;ltig zu l&ouml;schen oder wieder 
  herzustellen. Markieren Sie dazu das jeweilige Objekt und klicken sie dann auf 
  die entsprechende Funktion.<br>
  Der Inhalt des Papierkorbs ist personengebunden. Es werden dort nur Inhaltsobjekte 
  gespeichert die von Ihnen gel&ouml;scht wurden. Bitte leeren Sie den Papierkorb 
  gelegentlich aus um den Speicherplatz wieder freizugeben.</p>
