<p>Der Compose-Modus ist der Bereich indem Sie Inhalte einpflegen, &auml;ndern 
  oder l&ouml;schen. Wenn Sie den Compose-Modus betreten wird in der rechten Spalte 
  das Objekte-Fenster geladen. </p>
<p>&nbsp;</p>
<p>Das Objekte-Fenster</p>
<p class="headline"><a name="1"></a>Einf&uuml;geposition</p>
<p><img src="components/contents/help/gfx/insertpos.gif" width="141" height="24"></p>
<p>Mit der Einf&uuml;geposition geben Sie an wo Objekte eingef&uuml;gt werden 
  sollen, in unten am Ende der Seite, in der Mitte oder am Anfang der Seite.</p>
<p>&nbsp;</p>
<p class="bigheadline"><a name="2"></a>Inhalts-Objekte</p>
<p>Es gibt ein Standardset von Objekte die mit jeder Distribution von Liquido 
  mitgeliefert wird. Es ist aber auch m&ouml;glich individuelle Objekte zu erstellen. 
  Lesen Sie dazu die Dokumentation &quot;erstellen von Inhaltsobjekten&quot; im 
  Developer-Handbuch.</p>
<p> Die Inhaltsobjekte sind in Kategorien gegliedert. Durch das Klicken auf eine 
  Kategorie &ouml;ffnet sich diese und listet die Inhaltsobjekte auf. Wenn Sie 
  auf ein Inhaltsobjekt klicken wird es in Ihre Seite oder Gruppe an der angegebenen 
  Einf&uuml;geposition eingef&uuml;gt. Sie k&ouml;nnen die Postion eines Objektes 
  auch im Nachhinein &auml;ndern in dem Sie die Rangreihungsschaltfl&auml;chen 
  benutzen.</p>
<p>&nbsp;</p>
<p class="bigheadline"><a name="3"></a>Vorlagen</p>
<p>Sie k&ouml;nnen eine Seite als Vorlage speichern und diese sp&auml;ter in eine 
  andere Seite einf&uuml;gen. Das bietet sich an wenn man viele Seiten mit gleichem 
  Layout hat. Klicken Sie auf &quot;+ als Vorlage speichern&quot;, vergeben Sie 
  einen Namen f&uuml;r die Vorlage und best&auml;tigen Sie mit Enter. Die Vorlage 
  erscheint sofort in der Liste unter &quot;Vorlagen&quot;. Sie k&ouml;nnen Vorlagen 
  im Vorlagen-Modus auch bearbeiten oder l&ouml;schen.</p>
<p>&nbsp;</p>
<p class="bigheadline"><a name="4"></a>&Auml;nderungen speichern</p>
<p>Hiermit best&auml;tigen Sie &Auml;nderungen an der Seite. Best&auml;tigen Sie 
  jede &Auml;nderung bevor Sie ein neues Inhaltsobjekt hinzuf&uuml;gen, da sonst 
  m&ouml;glicherweise &Auml;nderungen verloren gehen.</p>
<p>&nbsp;</p>
<p class="bigheadline"><a name="5"></a>&quot;nach Unten-Schaltfl&auml;che&quot;</p>
<p>Unter der &quot;&Auml;nderungen speichern&quot;-Schaltfl&auml;che befindet 
  sich einr schmale Schaltfl&auml;che mit einem Pfeil nach unten. Damit bewegen 
  Sie die Seite in der Arbeitsfl&auml;che nach unten. Dadurch brauchen Sie bei 
  l&auml;ngeren Inhalten nicht nach jeder &Auml;nderung zum Ende der Arbeitsfl&auml;che 
  scrollen. </p>
<p>&nbsp;</p>
<p class="bigheadline"><a name="6"></a>Arbeitsfl&auml;che</p>
<p>In der Arbeitsfl&auml;che f&uuml;gen Sie die Inhaltsobjekte zu Inhalten zusammen. 
  Sie k&ouml;nnen die Objekte untereinander verschieben oder l&ouml;schen. Einige 
  Objekte haben ein Optionsmenu. Dieses ist zugeklappt und kann mit dem Schalter 
  <img src="components/contents/gfx/more.gif" width="19" height="16"> ge&ouml;ffnet 
  werden. </p>
<p>&nbsp;</p>
<p class="bigheadline"><a name="7"></a>Mehrfaches Bearbeiten verhindern</p>
<p>Liquido ist standardm&auml;ssig so konfiguriert, das es nicht m&ouml;glich 
  ist eine Seite oder eine Gruppe zu bearbeiten die bereits von einem anderen 
  Redaktuer bearbeitet wird. In diesem Fall erscheint eine Meldung wer den Inhalt 
  bearbeitet, wie lange schon und die Email-Adresse unter der der Redakteuer zu 
  erreichen ist. Wenn Der Redakteur seit mehr als 4 Stunden die Seite bearbeitet 
  wird die automatisch wieder freigegeben. Wenn Sie das Recht besitzen k&ouml;nnen 
  Sie die Bearbeitung auch einfach &quot;&uuml;bernehmen&quot;. Klicken Sie dazu 
  auf &quot;Bearbeiten &uuml;bernehmen&quot; und best&auml;tigen Sie mit &quot;ok&quot;. 
</p>
<p>Die Funktion zum Unterbinden von mehreren Bearbeitern kann in den Konfigurationen 
  von Liquido abgeschaltet werden.</p>

