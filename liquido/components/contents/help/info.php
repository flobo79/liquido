<p class="bigheadline">Modus &quot;Informationen&quot;</p>
<p>Im Modus Informationen k&ouml;nnen Sie die Eigenschaften von Inhaltsobjekten 
  bearbeiten, Inhalte l&ouml;schen oder erstellen oder die Ver&ouml;ffentlichung 
  festlegen.</p>
