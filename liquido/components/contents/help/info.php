<p>&nbsp;</p>
<p class="bigheadline">Objektdetails</p>
<p><img src="components/contents/help/gfx/details.gif" alt="details"></p>
<p>Die Objektdetails beinhalten den Titel, der fett dargestellt wird, die Objektid 
  und den Status, das Erstellungsdatum und das &Auml;nderungsdatum. Wenn zum Objekt 
  eine Information hinterlegt wurde wird diese mit dem vorangestellten Symbol 
  <img src="gfx/info2.gif" width="15" height="15"> dargestellt.</p>
<p>&nbsp;</p>
<p class="bigheadline">Eigenschaften von Seiten</p>
<p>Hier k&ouml;nnen Sie Seitenbezeichnung &auml;ndern, Infotexte 
  hinzuf&uuml;gen, die Seite in eine andere Seite oder Gruppe verschieben, oder 
  die &quot;eingeschr&auml;nkt&quot;-Eigenschaft festlegen. Diese dient f&uuml;r 
  Classes dazu diese Seite gesondert zu behandeln. Best&auml;tigen Sie Ihre &Auml;nderungen 
  mit &quot;speichern&quot;.</p>
<p class="bigheadline">&nbsp;</p>
<p class="bigheadline">Seite l&ouml;schen / Gruppe l&ouml;schen</p>
<p>Wenn Sie die Seite oder Gruppe l&ouml;schen wird sie zun&auml;chst in den Papierkorb 
  verschoben.</p>
<p>&nbsp;</p>
<p class="bigheadline">ver&ouml;ffentlichen</p>
<p>Hier k&ouml;nnen Sie die Seite oder Gruppe ver&ouml;ffentlichen oder wieder 
  offline schalten. Optional k&ouml;nnen Sie auch festlegen ob diese &Auml;nderung 
  f&uuml;r alle untergeordneten Seiten oder Gruppen geschehen soll.</p>
<p>Wenn die Funktion freigeschaltet ist, k&ouml;nnen Sie das ver&ouml;ffentlichen 
  oder offline-schalten auch zeitgesteuert durchf&uuml;hren lassen. Geben Sie 
  dazu das Datum f&uuml;r den Zeitpunkt der Ver&ouml;ffentlichung und/oder der 
  offline-Schaltung an.</p>
<p>Best&auml;tigen Sie Ihre Eingaben mit &quot;speichern&quot;.</p>
<p>&nbsp;</p>
<p class="bigheadline">Eigenschaften von Gruppen</p>
<p>Hier k&ouml;nnen Sie den Titel der Gruppe &auml;ndern, Informationen hinzuf&uuml;gen, 
  die Gruppe in eine andere Gruppe oder Seite verschieben und die Gruppe mit der 
  Eigenschaft &quot;eingeschr&auml;nkt&quot; kennzeichnen. Ausserdem geben Sie 
  bei Gruppen an wie breit der Inhalt sein soll. Auf diese Angabe beziehen sich 
  alle Unterliegenen Seiten. Achtung: geben Sie die Breite in Pixeln an.</p>
<p>Best&auml;tigen Sie ihre Eingaben mit &quot;speichern&quot;.</p>
<p>&nbsp;</p>
<p class="bigheadline">Auswahl einer Vorlage einer Gruppe</p>
<p>Hier w&auml;hlen Sie aus welche Vorlage eine Gruppe und damit alle untergeordneten 
  Seiten verwenden soll. Vorlagen definieren Sie im Bereich &quot;Vorlagen&quot;.</p>
<p>&nbsp;</p>
<p class="bigheadline">Anordnen von Inhalten</p>
<p>Sie k&ouml;nnen die Reihenfolge von Inhalten &auml;ndern indem Sie sie mit 
  den Rangreihungsschaltfl&auml;chen <img src="components/contents/help/gfx/move.gif" width="32" height="18"> 
  anordnen. </p>
